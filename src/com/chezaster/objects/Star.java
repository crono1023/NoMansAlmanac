package com.chezaster.objects;


public class Star {
	private String name;
	private StarClass strClass;
	
	public Star(String name, StarClass strClass) {
		this.name = name;
		this.strClass = strClass;
	}

	public String getName() {
		return name;
	}

	public StarClass getStrClass() {
		return strClass;
	}
	
	
	
	

}
